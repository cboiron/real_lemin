#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	i = 0;
	str = malloc(sizeof(char) * size + 1);
	if (str == NULL)
		return (NULL);
	while (i < size + 1)
		str[i++] = '\0';
	return (str);
}
